#WAP TO CONVERT FROM STRING TO TUPLE,SET,LIST 

s = 'PYTHON'

# Type casting from string()->tuple()
c = tuple(s) 
print ("After converting string to tuple : ",c)

# Type casting from string()->set()
c = set(s)  
print ("After converting string to set : ",c)
                                                                                                                       
# Type casting from string()->list()
c = list(s)                                                                                         
print ("After converting string to list : ",c)                   
