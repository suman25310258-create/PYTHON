#WAP TO EXAMINE WHICH LIST IS GREATER

#EXAMPLE:1
a=[3,4,5]
b=[3,4,2]
print(a>b) #True --> Because 5 >2

#EXAMPLE:2
a=[1,2,3]
b=[1,2,3,4]
print(a>b) #False --> a is shorter and all elements so far are equal

#EXAMPLE:3
a=[10,20,30,40]
b=[2,25,35,60]
print(a>b)  #TRUE --> Because 10 >2

#EXAMPLE:4
a=[1,2]
b=[1,3]
print(a>b)  #FALSE --> Because 2 <3

#EXAMPLE:5
a=[1,2,3,4]
b=[1,2,3,4]
print(a>b)  #FALSE --> Because same value (a=b)
