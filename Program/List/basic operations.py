#WAP TO SHOW FOLLOWING BASIC OPERATION ON LISTS

l1=[2,3,5,7,8]
l2=[1,4,6,9]

#List Concatination using '+' operator
l=l1+l2
print(l)

#Membership Operator
lst=[1,2,3,4,5,6]
r1=[1,2,3] in lst
print(r1)
r2=[1,2,3] not in lst
print(r2)
r3=5 in lst
print(r3)

#List Multiplication using '*' operator
mul1=l1*3
print(mul1)
mul2=2*l2
print(mul2)

